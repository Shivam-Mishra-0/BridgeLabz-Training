// Creating class with name AgeOfHarry indicating the purpose to calculate the age of Harry in 2024. 

public class AgeOfHarry
{
	public static void main(String args[])
	{	
		// Create a integer variable name with birthYear, currentYear and assign value 2000 & 2024.
		int birthYear = 2000,  currentYear = 2024;
		
		//Create a integer variable name with age. Subtraction operator (-). 
		int age = currentYear - birthYear ;

		//Print the current age of Harry.
		System.out.println("Harry's age in 2024 is " + age);
		
	}
}
	
